    public void mostrarDatos(){
        String [] filas = new String[4];
        String [] titulos = {"Id", "Tipo", "Cantidad", "Denominaci√≥n"}; 
        DefaultTableModel model = new DefaultTableModel(titulos,0);
        double total=0.0;
        Conexion_db consulta = new Conexion_db();
        try {
            consulta.conectar();
            ResultSet rs = consulta.consultar("SELECT * FROM Dinero ORDER BY Denominacion DESC;");
            while (rs.next()) {
                filas[0] = rs.getString("Id");
                filas[1] = rs.getString("Tipo");
                filas[2] = rs.getString("Cantidad");
                filas[3] = rs.getString("Denominacion");
                total+= Double.parseDouble(filas[2]) * Double.parseDouble(filas[3]); 
                model.addRow(filas);
            }
            consulta.cerrar();
        } catch (Exception ex) {
            System.err.println(ex);
        }
        jTable1.setModel(model);
        labelTotal.setText("Total= "+String.valueOf(total));
    }
    
